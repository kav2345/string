package string;
class car {
	private String name;
	private String color;
	private int cost;
	private int topspeed;
	private String country;
		car(String a,String b,int c,int d,String e){
			super();
			name=a;
			color=b;
			cost=c;
			topspeed=d;
			country=e;
		}
			public String getName() {
				return name;
			}
			public String getColor() {
				return color;
			}
			public int getCost() {
				return cost;
			}
			public int getTopspeed() {
				return topspeed;
			}
			public String getCountry() {
				return country;
			}
		}
		
	
